INIT:
	;; Reset USART
	MVI	A,00H
	OUT	USARTC
	MVI	A,00H
	OUT	USARTC
	MVI	A,00H
	OUT	USARTC
	MVI	A,40H
	OUT	USARTC

	;; 
	MVI	A,4EH
	OUT	USARTC

	;; 
	MVI	A,37H
	OUT	USARTC

	RET

CONIN:
	IN	USARTC
	ANI	02H
	JZ	CONIN
	IN	USARTD
	
	RET

CONST:
	IN	USARTC
	ANI	02H

	RET

CONOUT:
	PUSH	PSW
COUT0:
	IN	USARTC
	ANI	01H
	JZ	COUT0
	POP	PSW
	OUT	USARTD
	
	RET
